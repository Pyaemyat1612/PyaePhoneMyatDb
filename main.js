/* ============================================================
   HEADER — sticky shrink + mobile nav toggle
   ============================================================ */
function initHeader() {
  const header = document.getElementById("site-header");
  const toggle = document.getElementById("nav-toggle");
  const toggleIcon = toggle ? toggle.querySelector("i") : null;

  // Mobile nav toggle
  if (toggle && header) {
    toggle.addEventListener("click", () => {
      const open = header.classList.toggle("nav-open");
      if (toggleIcon) {
        toggleIcon.className = open ? "ri-close-line" : "ri-menu-line";
      }
    });

    // Close nav when a link is clicked
    header.querySelectorAll(".main-nav a").forEach(link => {
      link.addEventListener("click", () => {
        header.classList.remove("nav-open");
        if (toggleIcon) toggleIcon.className = "ri-menu-line";
      });
    });
  }

  // Close nav on outside click
  document.addEventListener("click", e => {
    if (header && header.classList.contains("nav-open") && !header.contains(e.target)) {
      header.classList.remove("nav-open");
      if (toggleIcon) toggleIcon.className = "ri-menu-line";
    }
  });
}


/* ============================================================
   CONTACT FORM
   ============================================================ */
function initContactForm() {
  const form   = document.getElementById("contact-form");
  const status = document.getElementById("form-status");
  if (!form || !status) return;

  form.addEventListener("submit", e => {
    e.preventDefault();

    const name    = form.name.value.trim();
    const email   = form.email.value.trim();
    const message = form.message.value.trim();

    // Basic validation
    if (!name || !email || !message) {
      setStatus("Please fill in all required fields.", "error");
      return;
    }

    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
      setStatus("Please enter a valid email address.", "error");
      return;
    }

    // Simulate send (replace with real fetch/EmailJS/Formspree as needed)
    const btn = form.querySelector("button[type=submit]");
    btn.disabled = true;
    btn.innerHTML = '<i class="ri-loader-4-line"></i> Sending…';

    setTimeout(() => {
      setStatus("✓ Message sent! I'll get back to you shortly.", "success");
      form.reset();
      btn.disabled = false;
      btn.innerHTML = '<i class="ri-send-plane-2-line"></i> Send Message';
    }, 1200);
  });

  function setStatus(msg, type) {
    status.textContent = msg;
    status.className   = "form-status " + type;
  }
}


/* ============================================================
   SCROLL-REVEAL  (simple IntersectionObserver)
   ============================================================ */
function initScrollReveal() {
  const style = document.createElement("style");
  style.textContent = `
    .reveal { opacity: 0; transform: translateY(1.5rem); transition: opacity .6s ease, transform .6s ease; }
    .reveal.visible { opacity: 1; transform: none; }
  `;
  document.head.appendChild(style);

  const targets = document.querySelectorAll(
    ".stat-card, .timeline-card, .project-card, .cert-card, .edu-card, " +
    ".hard-skill-item, .soft-skill-item, .contact-item"
  );

  targets.forEach((el, i) => {
    el.classList.add("reveal");
    el.style.transitionDelay = (i % 4) * 0.08 + "s";
  });

  const observer = new IntersectionObserver(
    entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12 }
  );

  targets.forEach(el => observer.observe(el));
}


/* ============================================================
   ACTIVE NAV HIGHLIGHT  (scroll spy)
   ============================================================ */
function initScrollSpy() {
  const sections = document.querySelectorAll("section[id]");
  const navLinks = document.querySelectorAll(".main-nav a");

  const style = document.createElement("style");
  style.textContent = `.main-nav a.active { color: var(--primary-400); }`;
  document.head.appendChild(style);

  const observer = new IntersectionObserver(
    entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          navLinks.forEach(link => {
            link.classList.toggle(
              "active",
              link.getAttribute("href") === "#" + entry.target.id
            );
          });
        }
      });
    },
    { rootMargin: "-40% 0px -55% 0px" }
  );

   
  sections.forEach(s => observer.observe(s));
}


